#!/usr/bin/env bash
set -euo pipefail

POTATO_BASE_DIR="${POTATO_BASE_DIR:-/opt/potato}"
POTATO_APP_DIR="${POTATO_APP_DIR:-${POTATO_BASE_DIR}/app}"
POTATO_VENV_DIR="${POTATO_VENV_DIR:-${POTATO_BASE_DIR}/venv}"
POTATO_WEB_HOST="${POTATO_WEB_HOST:-127.0.0.1}"
POTATO_WEB_PORT="${POTATO_WEB_PORT:-1983}"

export POTATO_BASE_DIR
export POTATO_MODEL_PATH="${POTATO_MODEL_PATH:-${POTATO_BASE_DIR}/models/Qwen3.5-2B-Q4_K_M.gguf}"
export POTATO_DOWNLOAD_STATE_PATH="${POTATO_DOWNLOAD_STATE_PATH:-${POTATO_BASE_DIR}/state/download.json}"
export POTATO_ENSURE_MODEL_SCRIPT="${POTATO_ENSURE_MODEL_SCRIPT:-${POTATO_BASE_DIR}/bin/ensure_model.sh}"
export POTATO_START_LLAMA_SCRIPT="${POTATO_START_LLAMA_SCRIPT:-${POTATO_BASE_DIR}/bin/start_llama.sh}"
export POTATO_LLAMA_BASE_URL="${POTATO_LLAMA_BASE_URL:-http://127.0.0.1:8080}"
export POTATO_CHAT_BACKEND="${POTATO_CHAT_BACKEND:-llama}"
export POTATO_ALLOW_FAKE_FALLBACK="${POTATO_ALLOW_FAKE_FALLBACK:-0}"
export POTATO_LLAMA_RUNTIME_DIR="${POTATO_LLAMA_RUNTIME_DIR:-${POTATO_BASE_DIR}/llama}"
export POTATO_MMPROJ_PATH="${POTATO_MMPROJ_PATH:-}"

mkdir -p "${POTATO_BASE_DIR}/state" "${POTATO_BASE_DIR}/models" "${POTATO_BASE_DIR}/config"

cd "${POTATO_APP_DIR}"
exec "${POTATO_VENV_DIR}/bin/uvicorn" main:app --host "${POTATO_WEB_HOST}" --port "${POTATO_WEB_PORT}"
